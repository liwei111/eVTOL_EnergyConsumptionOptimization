% Estimate cruise performance
%
%W
function cruiseOutput= cruisePower(V,W0,rProp,nMotor_r,PMotor_r,takeoffHoverOutput)
AR=9;
%
CD0=0.025;
%
e=1.78*(1-0.045*AR^0.68)-0.64;
%
k=1/(pi*AR*e);
%
rho=1.125;
%
cr=1.49;
%
ct=0.79;
%
b=10.4;
%m^2
S=b*(ct+cr);
%
CL=0.5;
%
CD=CD0+k*CL^2;
%
D=0.5*rho*S*V^2*CD;
%
etaESC=0.85;
%
etaProp=0.8;
%
Ct=1.5;
%
etaMotor=motor_eta_cal(sqrt(D/(5*Ct*rho*(2*rProp)^4))*60,D*V/etaProp/5,takeoffHoverOutput.Pm_max/8,nMotor_r,PMotor_r*1000);
%
cruiseOutput.Pm=D*V/etaProp;
cruiseOutput.Pb=D*V/(etaMotor*etaESC*etaProp);
end

